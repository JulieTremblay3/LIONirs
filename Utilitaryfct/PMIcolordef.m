function color = PMIcolordef(NbCH)
NbCH_half = NbCH/2;
colordef = [0,0,143/256;    %Bleu foncé
            0,207/255,255/255;
            85/255,255/255,255/255;
            0,85/255 ,255/255;          %Bleu pale
            85/255, 85/255,255/255;
            0, 128/255,102/255;
            80/255, 240/255, 177/255;
            80/255, 240/255, 50/255;       
            2/255, 228/255, 120/255;
            160/255, 0, 120/255;      
            20/255, 240/255, 60/255;
            255/255,0,0;
            128/255,0,0;
            128/255,0, 128/255;
            255/255,0,128/255;
            255/255,153/255,0;
            81/255, 81/255, 113/255;
            0, 128/255,0]; 
        color = [];
        while size(color,1) < NbCH_half
            color = cat(1,color,colordef);
        end
        color = [color(1:NbCH_half,:);color(1:NbCH_half,:)];